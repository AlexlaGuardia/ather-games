// --- Stub map generator for testing ---
// Creates bordered grass maps with warp openings at specified positions

interface MapOpening {
  side: 'north' | 'south' | 'east' | 'west'
  positions: number[] // col indices for N/S, row indices for E/W
}

function createStubMap(cols: number, rows: number, openings: MapOpening[] = []): number[][] {
  // Border tile values (rotation-encoded: (rot << 8) | tileIdx)
  const TL = 12, TOP = 267, TR = 268
  const LEFT = 11, RIGHT = 523
  const BL = 780, BOTTOM = 779, BR = 524
  const WARP = 14

  const isOpen = (side: string, pos: number) =>
    openings.some(o => o.side === side && o.positions.includes(pos))

  const grid: number[][] = []
  for (let y = 0; y < rows; y++) {
    const row: number[] = []
    for (let x = 0; x < cols; x++) {
      if (y === 0) {
        if (x === 0) row.push(isOpen('north', x) ? WARP : TL)
        else if (x === cols - 1) row.push(isOpen('north', x) ? WARP : TR)
        else row.push(isOpen('north', x) ? WARP : TOP)
      } else if (y === rows - 1) {
        if (x === 0) row.push(isOpen('south', x) ? WARP : BL)
        else if (x === cols - 1) row.push(isOpen('south', x) ? WARP : BR)
        else row.push(isOpen('south', x) ? WARP : BOTTOM)
      } else if (x === 0) {
        row.push(isOpen('west', y) ? WARP : LEFT)
      } else if (x === cols - 1) {
        row.push(isOpen('east', y) ? WARP : RIGHT)
      } else {
        // Deterministic grass variation
        row.push(((x * 7 + y * 13) % 8 === 0) ? 1 : 0)
      }
    }
    grid.push(row)
  }
  return grid
}

